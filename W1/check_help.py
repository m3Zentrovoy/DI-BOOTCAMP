# Заготовки для проверки ввода пользователя

# 1. Проверка, что строка не пустая
# Использование: Убедиться, что пользователь ввёл что-то, а не просто нажал Enter
user_input = input("Введите что-нибудь: ")
if not user_input:
    print("Ошибка: ввод не может быть пустым!")

# 2. Проверка на пробелы (пустая строка или только пробелы)
# Использование: Убедиться, что ввод не состоит только из пробелов
user_input = input("Введите что-нибудь: ")
if not user_input or user_input.isspace():
    print("Ошибка: введите что-то, а не только пробелы!")

# 3. Проверка, что введённый символ — буква (a-z, A-Z)
# Использование: Для игр или задач, где нужно вводить только буквы
user_input = input("Введите букву: ")
if not user_input.isalpha():
    print("Ошибка: введите букву (a-z, A-Z)!")

# 4. Проверка длины ввода (например, ровно 1 символ)
# Использование: Убедиться, что ввели ровно одну букву или символ
user_input = input("Введите один символ: ")
if len(user_input) != 1:
    print("Ошибка: введите ровно один символ!")

# 5. Проверка, что введено число
# Использование: Для ввода чисел, например, номера клетки в игре
try:
    number = int(input("Введите число: "))
except ValueError:
    print("Ошибка: введите действительное число!")

# 6. Проверка, что число в нужном диапазоне
# Использование: Убедиться, что число находится в допустимом диапазоне (например, 1-9)
try:
    number = int(input("Введите число от 1 до 9: "))
    if number < 1 or number > 9:
        print("Ошибка: введите число от 1 до 9!")
except ValueError:
    print("Ошибка: введите действительное число!")

# 7. Проверка на заглавные буквы
# Использование: Убедиться, что строка состоит только из заглавных букв
user_input = input("Введите текст заглавными буквами: ")
if not user_input.isupper():
    print("Ошибка: введите только заглавные буквы!")

# 8. Проверка на строчные буквы
# Использование: Убедиться, что строка состоит только из строчных букв
user_input = input("Введите текст строчными буквами: ")
if not user_input.islower():
    print("Ошибка: введите только строчные буквы!")

# 9. Проверка, что строка содержит только буквы и пробелы
# Использование: Для ввода фраз, где допустимы только буквы и пробелы
user_input = input("Введите фразу (только буквы и пробелы): ")
if not all(char.isalpha() or char.isspace() for char in user_input):
    print("Ошибка: введите только буквы и пробелы!")

# 10. Проверка, что строка не содержит цифр
# Использование: Убедиться, что в строке нет цифр
user_input = input("Введите текст без цифр: ")
if any(char.isdigit() for char in user_input):
    print("Ошибка: в тексте не должно быть цифр!")